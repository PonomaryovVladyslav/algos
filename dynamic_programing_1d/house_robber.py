def rob(nums: list[int]) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –Ω–∞—Ö–æ–¥–∏—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —Å—É–º–º—É –¥–µ–Ω–µ–≥, –∫–æ—Ç–æ—Ä—É—é –º–æ–∂–Ω–æ —É–∫—Ä–∞—Å—Ç—å,
    –µ—Å–ª–∏ –Ω–µ–ª—å–∑—è –≥—Ä–∞–±–∏—Ç—å –¥–≤–∞ —Å–æ—Å–µ–¥–Ω–∏—Ö –¥–æ–º–∞.

    üîπ –ê–ª–≥–æ—Ä–∏—Ç–º:
    1Ô∏è‚É£ –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ `nums` –ø—É—Å—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º `0`.
    2Ô∏è‚É£ –ï—Å–ª–∏ `len(nums) == 1`, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ (–Ω–µ—Ç —Å–æ—Å–µ–¥–µ–π).
    3Ô∏è‚É£ –ò—Å–ø–æ–ª—å–∑—É–µ–º –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–µ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ:
        - `prev1 = dp[i-1]` (–ø—Ä–µ–¥—ã–¥—É—â–∏–π –¥–æ–º)
        - `prev2 = dp[i-2]` (–¥–æ–º –ø–µ—Ä–µ–¥ –ø—Ä–µ–¥—ã–¥—É—â–∏–º)
    4Ô∏è‚É£ –î–ª—è –∫–∞–∂–¥–æ–≥–æ –¥–æ–º–∞:
        - –†–µ—à–∞–µ–º, –±—Ä–∞—Ç—å –ª–∏ —Ç–µ–∫—É—â–∏–π –¥–æ–º `num` (`prev2 + num`) –∏–ª–∏ –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å (`prev1`).
        - `new_rob = max(prev1, prev2 + num)`
        - –û–±–Ω–æ–≤–ª—è–µ–º `prev1` –∏ `prev2`.
    5Ô∏è‚É£ –ò—Ç–æ–≥–æ–≤—ã–π `prev1` ‚Äî –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞, –∫–æ—Ç–æ—Ä—É—é –º–æ–∂–Ω–æ —É–∫—Ä–∞—Å—Ç—å.

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(n)** ‚Äî –æ–¥–∏–Ω –ø—Ä–æ—Ö–æ–¥ –ø–æ `nums`.
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(1)** ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–æ–ª—å–∫–æ –¥–≤–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ.

    :param nums: list[int] - —Å–ø–∏—Å–æ–∫ –∑–Ω–∞—á–µ–Ω–∏–π –≤ –¥–æ–º–∞—Ö.
    :return: int - –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞, –∫–æ—Ç–æ—Ä—É—é –º–æ–∂–Ω–æ —É–∫—Ä–∞—Å—Ç—å.
    """

    if not nums:
        return 0
    if len(nums) == 1:
        return nums[0]

    prev1, prev2 = 0, 0  # prev1 = dp[i-1], prev2 = dp[i-2]

    for num in nums:
        new_rob = max(prev1, prev2 + num)  # –í—ã–±–∏—Ä–∞–µ–º –º–∞–∫—Å: –≤–∑—è—Ç—å —Ç–µ–∫—É—â–∏–π –∏–ª–∏ –Ω–µ—Ç
        prev2 = prev1  # –û–±–Ω–æ–≤–ª—è–µ–º prev2
        prev1 = new_rob  # –û–±–Ω–æ–≤–ª—è–µ–º prev1

    return prev1  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(rob([1, 2, 3, 1]))  # 4
print(rob([2, 7, 9, 3, 1]))  # 12
print(rob([5, 3, 4, 11, 2]))  # 16
print(rob([100, 200, 300, 400, 500]))  # 900
print(rob([2, 1, 1, 2]))  # 4
