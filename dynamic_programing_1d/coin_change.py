def coin_change(coins: list[int], amount: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –¥–ª—è –Ω–∞—Ö–æ–∂–¥–µ–Ω–∏—è –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –º–æ–Ω–µ—Ç –¥–ª—è —Å—É–º–º—ã `amount`.

    üîπ –ê–ª–≥–æ—Ä–∏—Ç–º:
    1Ô∏è‚É£ –°–æ–∑–¥–∞—ë–º –º–∞—Å—Å–∏–≤ `dp`, –≥–¥–µ `dp[i]` ‚Äî –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–æ–Ω–µ—Ç –¥–ª—è —Å—É–º–º—ã `i`.
    2Ô∏è‚É£ –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º `dp` –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç—è–º–∏ (`float('inf')`), —Ç–∞–∫ –∫–∞–∫ –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ —Å—É–º–º–∞ —Å—á–∏—Ç–∞–µ—Ç—Å—è –Ω–µ–¥–æ—Å—Ç–∏–∂–∏–º–æ–π.
    3Ô∏è‚É£ `dp[0] = 0`, —Ç–∞–∫ –∫–∞–∫ –¥–ª—è —Å—É–º–º—ã `0` –º–æ–Ω–µ—Ç—ã –Ω–µ –Ω—É–∂–Ω—ã.
    4Ô∏è‚É£ –î–ª—è –∫–∞–∂–¥–æ–π –º–æ–Ω–µ—Ç—ã `coin` –∏–∑ —Å–ø–∏—Å–∫–∞ `coins`:
        - –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ `dp[i]`, –Ω–∞—á–∏–Ω–∞—è —Å `i = coin`, –∏ –æ–±–Ω–æ–≤–ª—è–µ–º `dp[i]`:
          `dp[i] = min(dp[i], dp[i - coin] + 1)`
        - –≠—Ç–æ –æ–∑–Ω–∞—á–∞–µ—Ç, —á—Ç–æ –º—ã –ª–∏–±–æ –æ—Å—Ç–∞–≤–ª—è–µ–º —Å—Ç–∞—Ä–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ `dp[i]`, –ª–∏–±–æ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–µ–∫—É—â—É—é –º–æ–Ω–µ—Ç—É.
    5Ô∏è‚É£ –ï—Å–ª–∏ `dp[amount] == float('inf')`, —Ç–æ —Å—É–º–º—É –Ω–µ–ª—å–∑—è –Ω–∞–±—Ä–∞—Ç—å, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º `-1`.

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(n * m)**, –≥–¥–µ `n = amount`, `m = len(coins)`.
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(n)**, —Ç–∞–∫ –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –º–∞—Å—Å–∏–≤ `dp` —Ä–∞–∑–º–µ—Ä–æ–º `amount + 1`.

    :param coins: list[int] - —Å–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –Ω–æ–º–∏–Ω–∞–ª–æ–≤ –º–æ–Ω–µ—Ç.
    :param amount: int - —Ü–µ–ª–µ–≤–∞—è —Å—É–º–º–∞.
    :return: int - –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–æ–Ω–µ—Ç –¥–ª—è —Å–æ—Å—Ç–∞–≤–ª–µ–Ω–∏—è `amount`, –ª–∏–±–æ -1, –µ—Å–ª–∏ –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ.
    """

    # 1. –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –º–∞—Å—Å–∏–≤ dp (–±–µ—Å–∫–æ–Ω–µ—á–Ω–æ—Å—Ç—è–º–∏)
    dp = [float('inf')] * (amount + 1)
    dp[0] = 0  # 0 –º–æ–∂–Ω–æ —Å–æ–±—Ä–∞—Ç—å –±–µ–∑ –º–æ–Ω–µ—Ç

    # 2. –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –º–æ–Ω–µ—Ç—ã
    for coin in coins:
        for i in range(coin, amount + 1):  # –û–±–Ω–æ–≤–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è dp[i]
            dp[i] = min(dp[i], dp[i - coin] + 1)

    # 3. –ï—Å–ª–∏ `amount` –Ω–µ–ª—å–∑—è –Ω–∞–±—Ä–∞—Ç—å ‚Äî –≤–æ–∑–≤—Ä–∞—â–∞–µ–º -1
    return dp[amount] if dp[amount] != float('inf') else -1


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(coin_change([1, 2, 5], 11))  # 3 (5 + 5 + 1)
print(coin_change([2], 3))  # -1 (–Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ)
print(coin_change([1], 0))  # 0 (–Ω–æ–ª—å –º–æ–Ω–µ—Ç)
print(coin_change([1, 2, 5], 100))  # 20 (100 = 5*20)
