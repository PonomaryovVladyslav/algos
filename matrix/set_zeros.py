def set_zeros(matrix: list[list[int]]) -> None:
    """
    –û–±–Ω—É–ª—è–µ—Ç —Å—Ç—Ä–æ–∫–∏ –∏ —Å—Ç–æ–ª–±—Ü—ã, —Å–æ–¥–µ—Ä–∂–∞—â–∏–µ —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω `0`.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º (O(m √ó n))**:
       1. –û–ø—Ä–µ–¥–µ–ª—è–µ–º, –∫–∞–∫–∏–µ —Å—Ç—Ä–æ–∫–∏ –∏ —Å—Ç–æ–ª–±—Ü—ã —Å–æ–¥–µ—Ä–∂–∞—Ç `0`.
       2. –û–±–Ω—É–ª—è–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ —Å—Ç—Ä–æ–∫–∏ –∏ —Å—Ç–æ–ª–±—Ü—ã.

    üîπ **–°–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(m √ó n)` –ø–æ –≤—Ä–µ–º–µ–Ω–∏ (–ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–π –º–∞—Ç—Ä–∏—Ü–µ).
       - `O(m + n)` –ø–æ –ø–∞–º—è—Ç–∏ (–∏—Å–ø–æ–ª—å–∑—É–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–∞ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∏–Ω–¥–µ–∫—Å–æ–≤).
    """
    zero_rows = set()
    zero_cols = set()

    # 1. –ó–∞–ø–æ–º–∏–Ω–∞–µ–º, –≥–¥–µ –µ—Å—Ç—å `0`
    for i in range(len(matrix)):
        for j in range(len(matrix[0])):
            if matrix[i][j] == 0:
                zero_rows.add(i)
                zero_cols.add(j)

    # 2. –û–±–Ω—É–ª—è–µ–º —Å—Ç—Ä–æ–∫–∏
    for row in zero_rows:
        for j in range(len(matrix[0])):
            matrix[row][j] = 0

    # 3. –û–±–Ω—É–ª—è–µ–º —Å—Ç–æ–ª–±—Ü—ã
    for col in zero_cols:
        for i in range(len(matrix)):
            matrix[i][col] = 0


# üîπ –¢–µ—Å—Ç
matrix1 = [
    [0, 1, 2, 0],
    [3, 4, 5, 2],
    [1, 3, 1, 5]
]

set_zeros(matrix1)

# üîπ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç:
# [[0, 0, 0, 0],
#  [0, 4, 5, 0],
#  [0, 3, 1, 0]]

for row in matrix1:
    print(row)
