def my_pow(x: float, n: int) -> float:
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç `x^n` (x –≤ —Å—Ç–µ–ø–µ–Ω–∏ n) —Å –ø–æ–º–æ—â—å—é –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –±—ã—Å—Ç—Ä–æ–≥–æ –≤–æ–∑–≤–µ–¥–µ–Ω–∏—è –≤ —Å—Ç–µ–ø–µ–Ω—å.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º**:
       - –ï—Å–ª–∏ `n < 0`, –ø–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ–º `x` –∏ –¥–µ–ª–∞–µ–º `n` –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã–º.
       - –ò—Å–ø–æ–ª—å–∑—É–µ–º –º–µ—Ç–æ–¥ –¥–≤–æ–∏—á–Ω–æ–≥–æ —Ä–∞–∑–ª–æ–∂–µ–Ω–∏—è (`O(log n)`):
         - –ï—Å–ª–∏ `n` –Ω–µ—á–µ—Ç–Ω–æ–µ ‚Üí —É–º–Ω–æ–∂–∞–µ–º `result` –Ω–∞ `x`.
         - –£–º–Ω–æ–∂–∞–µ–º `x` —Å–∞–º–æ –Ω–∞ —Å–µ–±—è (`x *= x`).
         - –î–≤–∏–≥–∞–µ–º `n` –≤–ø—Ä–∞–≤–æ (—Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ `2`).

    üîπ **–°–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(log n)`, —Ç–∞–∫ –∫–∞–∫ `n` —É–º–µ–Ω—å—à–∞–µ—Ç—Å—è –≤–¥–≤–æ–µ –Ω–∞ –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏.
    """
    if n < 0:
        x = 1 / x
        n = -n

    result = 1
    while n:
        if n % 2:  # –ï—Å–ª–∏ n –Ω–µ—á–µ—Ç–Ω–æ–µ
            result *= x
        x *= x  # –£–º–Ω–æ–∂–∞–µ–º –æ—Å–Ω–æ–≤–∞–Ω–∏–µ –Ω–∞ —Å–µ–±—è
        n //= 2  # –î–≤–∏–≥–∞–µ–º n –≤–ø—Ä–∞–≤–æ (–¥–µ–ª–∏–º –Ω–∞ 2)

    return result


# üîπ –¢–µ—Å—Ç—ã
print(my_pow(2.0, 10))  # ‚úÖ 1024.0
print(my_pow(2.1, 3))  # ‚úÖ 9.261
print(my_pow(2.0, -2))  # ‚úÖ 0.25
print(my_pow(3.0, 5))  # ‚úÖ 243.0
print(my_pow(0.5, 2))  # ‚úÖ 0.25
print(my_pow(10, 0))  # ‚úÖ 1
print(my_pow(2, -3))  # ‚úÖ 0.125
