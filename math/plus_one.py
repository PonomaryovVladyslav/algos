def plus_one(digits: list[int]) -> list[int]:
    """
    –£–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç —á–∏—Å–ª–æ, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–Ω–æ–µ –º–∞—Å—Å–∏–≤–æ–º —Ü–∏—Ñ—Ä, –Ω–∞ 1.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º**:
       - –ò—Ç–µ—Ä–∏—Ä—É–µ–º –º–∞—Å—Å–∏–≤ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª–µ–≤–æ.
       - –ï—Å–ª–∏ —Ü–∏—Ñ—Ä–∞ `< 9`, –ø—Ä–æ—Å—Ç–æ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∏ –≤—ã—Ö–æ–¥–∏–º.
       - –ï—Å–ª–∏ —Ü–∏—Ñ—Ä–∞ `9`, –∑–∞–º–µ–Ω—è–µ–º –µ—ë –Ω–∞ `0` –∏ –ø–µ—Ä–µ–Ω–æ—Å–∏–º `1` –¥–∞–ª—å—à–µ.
       - –ï—Å–ª–∏ –≤—Å–µ —Ü–∏—Ñ—Ä—ã –±—ã–ª–∏ `9` (–Ω–∞–ø—Ä–∏–º–µ—Ä, `999` ‚Üí `1000`), –¥–æ–±–∞–≤–ª—è–µ–º `1` –≤ –Ω–∞—á–∞–ª–æ.

    üîπ **–°–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ –≤ —Ö—É–¥—à–µ–º —Å–ª—É—á–∞–µ –º—ã –ø—Ä–æ—Ö–æ–¥–∏–º –≤–µ—Å—å –º–∞—Å—Å–∏–≤ (–Ω–∞–ø—Ä–∏–º–µ—Ä, `[9,9,9]`).
    """
    for i in range(len(digits) - 1, -1, -1):
        if digits[i] < 9:  # –ï—Å–ª–∏ —Ü–∏—Ñ—Ä–∞ < 9, –ø—Ä–æ—Å—Ç–æ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º
            digits[i] += 1
            return digits
        digits[i] = 0  # –ò–Ω–∞—á–µ –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –≤ 0 –∏ –ø–µ—Ä–µ–Ω–æ—Å–∏–º 1 –¥–∞–ª—å—à–µ

    return [1] + digits  # –ï—Å–ª–∏ –≤—Å–µ —Ü–∏—Ñ—Ä—ã –±—ã–ª–∏ 9 (–Ω–∞–ø—Ä–∏–º–µ—Ä, 999 ‚Üí 1000)


# üîπ –¢–µ—Å—Ç—ã
print(plus_one([1,2,3]))  # ‚úÖ [1,2,4]
print(plus_one([9,9,9]))  # ‚úÖ [1,0,0,0]
print(plus_one([4,3,2,1]))  # ‚úÖ [4,3,2,2]
print(plus_one([0]))  # ‚úÖ [1]
print(plus_one([8,9,9]))  # ‚úÖ [9,0,0]
print(plus_one([9]))  # ‚úÖ [1,0]
