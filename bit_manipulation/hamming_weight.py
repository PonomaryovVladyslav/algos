# –ù—É–∂–Ω–æ: –≤–µ—Ä–Ω—É—Ç—å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ `1` –≤ `bin(n)`.

def hamming_weight(n: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è —Å—á–∏—Ç–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –µ–¥–∏–Ω–∏—á–Ω—ã—Ö –±–∏—Ç–æ–≤ –≤ —á–∏—Å–ª–µ (–∞–ª–≥–æ—Ä–∏—Ç–º –ë—Ä–∞–π–∞–Ω–∞ –ö–µ—Ä–Ω–∏–≥–∞–Ω–∞).

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(k), –≥–¥–µ `k` ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –µ–¥–∏–Ω–∏—Ü –≤ —á–∏—Å–ª–µ (–æ–±—ã—á–Ω–æ O(log n)).
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(1), —Ç–∞–∫ –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ –æ–¥–Ω–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è.

    :param n: int - –≤—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ.
    :return: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–∏—Ç–æ–≤, —Ä–∞–≤–Ω—ã—Ö 1.
    """

    count: int = 0
    while n:
        n &= (n - 1)  # –£–±–∏—Ä–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π 1
        count += 1
    return count


def hamming_weight_bin(n: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è —Å—á–∏—Ç–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –µ–¥–∏–Ω–∏—á–Ω—ã—Ö –±–∏—Ç–æ–≤ –≤ —á–∏—Å–ª–µ (—á–µ—Ä–µ–∑ `bin()`).

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(log n), —Ç–∞–∫ –∫–∞–∫ `bin(n)` –∏—Å–ø–æ–ª—å–∑—É–µ—Ç O(log n) –æ–ø–µ—Ä–∞—Ü–∏–π.
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(1), —Ç–∞–∫ –∫–∞–∫ `bin()` –∏—Å–ø–æ–ª—å–∑—É–µ—Ç —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω—É—é –ø–∞–º—è—Ç—å.

    :param n: int - –≤—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ.
    :return: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–∏—Ç–æ–≤, —Ä–∞–≤–Ω—ã—Ö 1.
    """

    return bin(n).count('1')


