# –ó–∞–¥–∞—á–∞: –ù–∞–π—Ç–∏ —á–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è **—Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω —Ä–∞–∑**.
# –î–∞–Ω–æ: –º–∞—Å—Å–∏–≤ `nums`, –≥–¥–µ –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –≤—Å—Ç—Ä–µ—á–∞—é—Ç—Å—è **—Ä–æ–≤–Ω–æ —Ç—Ä–∏ —Ä–∞–∑–∞**, –∫—Ä–æ–º–µ –æ–¥–Ω–æ–≥–æ.
# –ù—É–∂–Ω–æ: –≤–µ—Ä–Ω—É—Ç—å —ç—Ç–æ —É–Ω–∏–∫–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ.

def find_single_number(nums: list[int]) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –Ω–∞—Ö–æ–¥–∏—Ç —á–∏—Å–ª–æ, –≤—Å—Ç—Ä–µ—á–∞—é—â–µ–µ—Å—è –æ–¥–∏–Ω —Ä–∞–∑ (–∏—Å–ø–æ–ª—å–∑—É—è –±–∏—Ç–æ–≤—ã–µ –æ–ø–µ—Ä–∞—Ü–∏–∏).

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(n), —Ç–∞–∫ –∫–∞–∫ –º—ã –ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ –º–∞—Å—Å–∏–≤—É –æ–¥–∏–Ω —Ä–∞–∑.
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(1), —Ç–∞–∫ –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–æ–ª—å–∫–æ –¥–≤–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ.

    :param nums: list[int] - –º–∞—Å—Å–∏–≤, –≤ –∫–æ—Ç–æ—Ä–æ–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã –≤—Å—Ç—Ä–µ—á–∞—é—Ç—Å—è —Ç—Ä–∏–∂–¥—ã, –∫—Ä–æ–º–µ –æ–¥–Ω–æ–≥–æ.
    :return: int - —É–Ω–∏–∫–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ.
    """

    ones: int = 0  # –ß–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏–ª–æ—Å—å –æ–¥–∏–Ω —Ä–∞–∑
    twos: int = 0  # –ß–∏—Å–ª–æ, –∫–æ—Ç–æ—Ä–æ–µ –ø–æ—è–≤–∏–ª–æ—Å—å –¥–≤–∞ —Ä–∞–∑–∞

    for num in nums:
        ones = (ones ^ num) & ~twos  # –û–±–Ω–æ–≤–ª—è–µ–º `ones`, —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –µ—Å–ª–∏ —á–∏—Å–ª–æ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è 3 —Ä–∞–∑–∞
        twos = (twos ^ num) & ~ones  # –û–±–Ω–æ–≤–ª—è–µ–º `twos`, —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –µ—Å–ª–∏ —á–∏—Å–ª–æ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è 3 —Ä–∞–∑–∞

    return ones  # `ones` —Å–æ–¥–µ—Ä–∂–∏—Ç –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
nums1 = [2, 2, 3, 2]
nums2 = [0, 1, 0, 1, 0, 1, 99]
nums3 = [7, 7, 7, 5]

print(find_single_number(nums1))  # ‚úÖ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 3
print(find_single_number(nums2))  # ‚úÖ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 99
print(find_single_number(nums3))  # ‚úÖ –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 5


