def insert_interval(intervals: list[list[int]], new_interval: list[int]) -> list[list[int]]:
    """
    –í—Å—Ç–∞–≤–ª—è–µ—Ç –Ω–æ–≤—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –≤ —Å–ø–∏—Å–æ–∫ –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤ –∏ –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º**:
       - **–®–∞–≥ 1**: –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –∏–¥—É—Ç –¥–æ `new_interval` –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π.
       - **–®–∞–≥ 2**: –û–±—ä–µ–¥–∏–Ω—è–µ–º –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã —Å `new_interval`.
       - **–®–∞–≥ 3**: –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –æ—Å—Ç–∞–≤—à–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã –ø–æ—Å–ª–µ `new_interval`.

    üîπ **–í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ –º—ã –ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å–ø–∏—Å–∫—É `intervals` –æ–¥–∏–Ω —Ä–∞–∑.

    üîπ **–ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ –º—ã —Ö—Ä–∞–Ω–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º —Å–ø–∏—Å–∫–µ.

    :param intervals: list[list[int]] - –û—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã `[start, end]`.
    :param new_interval: list[int] - –ù–æ–≤—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª `[start, end]` –¥–ª—è –≤—Å—Ç–∞–≤–∫–∏.
    :return: list[list[int]] - –°–ø–∏—Å–æ–∫ –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω—ã—Ö –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤.
    """
    result = []
    i = 0
    n = len(intervals)

    # 1Ô∏è‚É£ –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –∏–¥—É—Ç –î–û `new_interval`
    while i < n and intervals[i][1] < new_interval[0]:
        result.append(intervals[i])
        i += 1

    # 2Ô∏è‚É£ –û–±—ä–µ–¥–∏–Ω—è–µ–º –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã —Å `new_interval`
    while i < n and intervals[i][0] <= new_interval[1]:
        new_interval[0] = min(new_interval[0], intervals[i][0])
        new_interval[1] = max(new_interval[1], intervals[i][1])
        i += 1
    result.append(new_interval)  # –î–æ–±–∞–≤–ª—è–µ–º –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª

    # 3Ô∏è‚É£ –î–æ–±–∞–≤–ª—è–µ–º –æ—Å—Ç–∞–≤—à–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã
    while i < n:
        result.append(intervals[i])
        i += 1

    return result


# üîπ –¢–µ—Å—Ç—ã
print(insert_interval([[1, 3], [6, 9]], [2, 5]))  # ‚úÖ [[1,5],[6,9]]
print(insert_interval([[1, 2], [3, 5], [6, 7], [8, 10], [12, 16]], [4, 8]))  # ‚úÖ [[1,2],[3,10],[12,16]]
print(insert_interval([], [5, 7]))  # ‚úÖ [[5,7]]
print(insert_interval([[1, 5]], [2, 3]))  # ‚úÖ [[1,5]]
print(insert_interval([[1, 5]], [6, 8]))  # ‚úÖ [[1,5], [6,8]]
