def merge_intervals(intervals: list[list[int]]) -> list[list[int]]:
    """
    –û–±—ä–µ–¥–∏–Ω—è–µ—Ç –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º**:
       1Ô∏è‚É£ –°–æ—Ä—Ç–∏—Ä—É–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã –ø–æ –Ω–∞—á–∞–ª—å–Ω–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é.
       2Ô∏è‚É£ –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–º –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞–º:
          - –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –ø–µ—Ä–µ—Å–µ–∫–∞–µ—Ç—Å—è —Å –ø–æ—Å–ª–µ–¥–Ω–∏–º –≤ `merged`, –æ–±—ä–µ–¥–∏–Ω—è–µ–º.
          - –ò–Ω–∞—á–µ, –¥–æ–±–∞–≤–ª—è–µ–º –∫–∞–∫ –Ω–æ–≤—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª.

    üîπ **–í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n log n)`, —Ç–∞–∫ –∫–∞–∫ —Ç—Ä–µ–±—É–µ—Ç—Å—è —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞.
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ –æ–¥–∏–Ω –ø—Ä–æ—Ö–æ–¥ –ø–æ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º—É —Å–ø–∏—Å–∫—É.

    üîπ **–ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫.

    :param intervals: list[list[int]] - –°–ø–∏—Å–æ–∫ –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤ `[start, end]`.
    :return: list[list[int]] - –°–ø–∏—Å–æ–∫ –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω—ã—Ö –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤.
    """
    if not intervals:
        return []

    # 1Ô∏è‚É£ –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ –Ω–∞—á–∞–ª—å–Ω–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é (O(n log n))
    intervals.sort()

    merged = [intervals[0]]  # –ù–∞—á–∏–Ω–∞–µ–º —Å –ø–µ—Ä–≤–æ–≥–æ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞

    for start, end in intervals[1:]:
        last_end = merged[-1][1]  # –ö–æ–Ω–µ—Ü –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞ –≤ `merged`

        if start <= last_end:  # –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ ‚Üí –æ–±—ä–µ–¥–∏–Ω—è–µ–º
            merged[-1][1] = max(last_end, end)
        else:
            merged.append([start, end])  # –ù–µ—Ç –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è ‚Üí –Ω–æ–≤—ã–π –∏–Ω—Ç–µ—Ä–≤–∞–ª

    return merged


# üîπ –¢–µ—Å—Ç—ã
print(merge_intervals([[1, 4], [5, 6]]))  # ‚úÖ [[1, 4], [5, 6]]
print(merge_intervals([[1, 3], [2, 6], [8, 10], [15, 18]]))  # ‚úÖ [[1, 6], [8, 10], [15, 18]]
print(merge_intervals([[1, 4], [4, 5]]))  # ‚úÖ [[1, 5]]
print(merge_intervals([[1, 10], [2, 6], [8, 10], [15, 18]]))  # ‚úÖ [[1, 10], [15, 18]]
print(merge_intervals([]))  # ‚úÖ []
print(merge_intervals([[1, 5]]))  # ‚úÖ [[1, 5]]
