def merge_intervals(intervals: list[list[int]]) -> list[list[int]]:
    """
    –û–±—ä–µ–¥–∏–Ω—è–µ—Ç –ø–µ—Ä–µ—Å–µ–∫–∞—é—â–∏–µ—Å—è –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º**:
       1Ô∏è‚É£ –°–æ—Ä—Ç–∏—Ä—É–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã –ø–æ –Ω–∞—á–∞–ª—å–Ω–æ–º—É –∑–Ω–∞—á–µ–Ω–∏—é.
       2Ô∏è‚É£ –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–º –∏–Ω—Ç–µ—Ä–≤–∞–ª–∞–º –∏:
          - –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –Ω–µ –ø–µ—Ä–µ—Å–µ–∫–∞–µ—Ç—Å—è —Å –ø–æ—Å–ª–µ–¥–Ω–∏–º –≤ `merged`, –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ.
          - –ï—Å–ª–∏ –ø–µ—Ä–µ—Å–µ–∫–∞–µ—Ç—Å—è, –æ–±—ä–µ–¥–∏–Ω—è–µ–º, —É–≤–µ–ª–∏—á–∏–≤–∞—è `end` –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ.

    üîπ **–í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n log n)`, —Ç–∞–∫ –∫–∞–∫ —Ç—Ä–µ–±—É–µ—Ç—Å—è —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞.
       - –ü–æ—Å–ª–µ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏, `O(n)` –¥–ª—è –æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏—è.

    üîπ **–ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, —Ç–∞–∫ –∫–∞–∫ —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫ –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞.

    :param intervals: list[list[int]] - –°–ø–∏—Å–æ–∫ –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤ `[start, end]`.
    :return: list[list[int]] - –°–ø–∏—Å–æ–∫ –æ–±—ä–µ–¥–∏–Ω—ë–Ω–Ω—ã—Ö –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–≤.
    """
    if not intervals:
        return []

    # 1Ô∏è‚É£ –°–æ—Ä—Ç–∏—Ä—É–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã –ø–æ –Ω–∞—á–∞–ª—å–Ω–æ–º—É –≤—Ä–µ–º–µ–Ω–∏
    intervals.sort(key=lambda x: x[0])

    merged = []
    for interval in intervals:
        # 2Ô∏è‚É£ –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç –∏–ª–∏ —Ç–µ–∫—É—â–∏–π –∏–Ω—Ç–µ—Ä–≤–∞–ª –ù–ï –ø–µ—Ä–µ—Å–µ–∫–∞–µ—Ç—Å—è —Å –ø–æ—Å–ª–µ–¥–Ω–∏–º
        if not merged or merged[-1][1] < interval[0]:
            merged.append(interval)
        else:
            # 3Ô∏è‚É£ –ï—Å–ª–∏ –µ—Å—Ç—å –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ, –æ–±—ä–µ–¥–∏–Ω—è–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã
            merged[-1][1] = max(merged[-1][1], interval[1])

    return merged


# üîπ –¢–µ—Å—Ç—ã
print(merge_intervals([[1, 3], [2, 6], [8, 10], [15, 18]]))  # ‚úÖ [[1, 6], [8, 10], [15, 18]]
print(merge_intervals([[1, 4], [4, 5]]))  # ‚úÖ [[1, 5]]
print(merge_intervals([[1, 10], [2, 6], [8, 10], [15, 18]]))  # ‚úÖ [[1, 10], [15, 18]]
print(merge_intervals([]))  # ‚úÖ []
print(merge_intervals([[1, 5]]))  # ‚úÖ [[1, 5]]
