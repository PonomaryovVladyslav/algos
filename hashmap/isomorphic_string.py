def is_isomorphic(s: str, t: str) -> bool:
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è—é—Ç—Å—è –ª–∏ –¥–≤–µ —Å—Ç—Ä–æ–∫–∏ –∏–∑–æ–º–æ—Ä—Ñ–Ω—ã–º–∏.

    üîπ **–û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ**:
       –î–≤–µ —Å—Ç—Ä–æ–∫–∏ –∏–∑–æ–º–æ—Ä—Ñ–Ω—ã, –µ—Å–ª–∏ —Å–∏–º–≤–æ–ª—ã –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏ –º–æ–∂–Ω–æ –≤–∑–∞–∏–º–Ω–æ –∑–∞–º–µ–Ω–∏—Ç—å
       –Ω–∞ —Å–∏–º–≤–æ–ª—ã –¥—Ä—É–≥–æ–π —Å—Ç—Ä–æ–∫–∏, —Å–æ—Ö—Ä–∞–Ω—è—è –ø–æ—Ä—è–¥–æ–∫, –±–µ–∑ –∫–æ–Ω—Ñ–ª–∏–∫—Ç–æ–≤.

    üîπ **–ê–ª–≥–æ—Ä–∏—Ç–º (O(n))**:
       1Ô∏è‚É£ –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –¥–ª–∏–Ω—ã —Å—Ç—Ä–æ–∫ —Å–æ–≤–ø–∞–¥–∞—é—Ç (`O(1)`).
       2Ô∏è‚É£ –°–æ–∑–¥–∞—ë–º –¥–≤–∞ —Å–ª–æ–≤–∞—Ä—è:
          - `map_s_t`: –°–æ–ø–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Å–∏–º–≤–æ–ª—ã `s` ‚Üí `t`
          - `map_t_s`: –°–æ–ø–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Å–∏–º–≤–æ–ª—ã `t` ‚Üí `s`
       3Ô∏è‚É£ –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å—Ç—Ä–æ–∫–∞–º (`O(n)`) –∏ –ø—Ä–æ–≤–µ—Ä—è–µ–º:
          - –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª —É–∂–µ –µ—Å—Ç—å –≤ `map_s_t`, –Ω–æ —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω —Å –¥—Ä—É–≥–∏–º ‚Üí `False`
          - –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª —É–∂–µ –µ—Å—Ç—å –≤ `map_t_s`, –Ω–æ —Å–æ–ø–æ—Å—Ç–∞–≤–ª–µ–Ω —Å –¥—Ä—É–≥–∏–º ‚Üí `False`
          - –ò–Ω–∞—á–µ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ –≤ –æ–±–∞ —Å–ª–æ–≤–∞—Ä—è.
       4Ô∏è‚É£ –ï—Å–ª–∏ –ø—Ä–æ—à–ª–∏ –≤—Å—é —Å—Ç—Ä–æ–∫—É –±–µ–∑ –∫–æ–Ω—Ñ–ª–∏–∫—Ç–æ–≤ ‚Üí `True`

    üîπ **–í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(n)`, –≥–¥–µ `n` ‚Äî –¥–ª–∏–Ω–∞ —Å—Ç—Ä–æ–∫–∏.

    üîπ **–ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å**:
       - `O(1)`, —Ç–∞–∫ –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å–ª–æ–≤–∞—Ä–∏ (–º–∞–∫—Å–∏–º—É–º 256 —Å–∏–º–≤–æ–ª–æ–≤).

    :param s: str - –ü–µ—Ä–≤–∞—è —Å—Ç—Ä–æ–∫–∞.
    :param t: str - –í—Ç–æ—Ä–∞—è —Å—Ç—Ä–æ–∫–∞.
    :return: bool - `True`, –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∏ –∏–∑–æ–º–æ—Ä—Ñ–Ω—ã, –∏–Ω–∞—á–µ `False`.
    """
    if len(s) != len(t):
        return False

    map_s_t = {}
    map_t_s = {}

    for char_s, char_t in zip(s, t):
        if (char_s in map_s_t and map_s_t[char_s] != char_t) or \
           (char_t in map_t_s and map_t_s[char_t] != char_s):
            return False  # –ù–∞—Ä—É—à–µ–Ω–æ —É–Ω–∏–∫–∞–ª—å–Ω–æ–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–µ

        map_s_t[char_s] = char_t
        map_t_s[char_t] = char_s

    return True


# üîπ –¢–µ—Å—Ç—ã
print(is_isomorphic("egg", "add"))      # ‚úÖ True  (e ‚Üí a, g ‚Üí d)
print(is_isomorphic("foo", "bar"))      # ‚úÖ False (f ‚Üí b, –Ω–æ o –Ω–µ–ª—å–∑—è –æ—Ç–æ–±—Ä–∞–∑–∏—Ç—å –Ω–∞ –¥–≤–µ –±—É–∫–≤—ã)
print(is_isomorphic("paper", "title"))  # ‚úÖ True  (p ‚Üí t, a ‚Üí i, e ‚Üí l, r ‚Üí e)
print(is_isomorphic("ab", "aa"))        # ‚úÖ False (a ‚Üí a, –Ω–æ b ‚Üí a –∫–æ–Ω—Ñ–ª–∏–∫—Ç—É–µ—Ç)
print(is_isomorphic("abcd", "baba"))    # ‚úÖ False (–∫–æ–Ω—Ñ–ª–∏–∫—Ç –≤ —Å–∏–º–≤–æ–ª–∞—Ö)

