# –ó–∞–¥–∞—á–∞: –°–ª–∏—Ç—å –¥–≤–∞ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –º–∞—Å—Å–∏–≤–∞ nums1 –∏ nums2 –≤ –æ–¥–∏–Ω, —Å–æ—Ö—Ä–∞–Ω—è—è –ø–æ—Ä—è–¥–æ–∫.
# –î–∞–Ω–æ:
# - nums1 –∏–º–µ–µ—Ç –¥–ª–∏–Ω—É m + n, –ø–µ—Ä–≤—ã–µ m —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å–æ–¥–µ—Ä–∂–∞—Ç —á–∏—Å–ª–∞, –æ—Å—Ç–∞–ª—å–Ω—ã–µ n —ç–ª–µ–º–µ–Ω—Ç–æ–≤ ‚Äî –Ω—É–ª–∏ (–º–µ—Å—Ç–æ –¥–ª—è nums2).
# - nums2 —Å–æ–¥–µ—Ä–∂–∏—Ç n —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –∏ –≤—Å–µ –æ–Ω–∏ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω—ã.
# –ù—É–∂–Ω–æ –æ–±—ä–µ–¥–∏–Ω–∏—Ç—å nums2 –≤ nums1 –≤ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.

def merge_sorted_arrays(nums1: list[int], nums2: list[int], m: int, n: int) -> None:
    """
    –§—É–Ω–∫—Ü–∏—è —Å–ª–∏–≤–∞–µ—Ç –¥–≤–∞ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –º–∞—Å—Å–∏–≤–∞ nums1 –∏ nums2 –≤ nums1.

    :param nums1: list[int] - –ø–µ—Ä–≤—ã–π –º–∞—Å—Å–∏–≤ –¥–ª–∏–Ω—ã (m + n), —Å–æ–¥–µ—Ä–∂–∞—â–∏–π m —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∏ n —Å–≤–æ–±–æ–¥–Ω—ã—Ö –º–µ—Å—Ç (–Ω—É–ª–µ–π).
    :param nums2: list[int] - –≤—Ç–æ—Ä–æ–π –º–∞—Å—Å–∏–≤ –¥–ª–∏–Ω—ã n, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π n –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤.
    :param m: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ñ–∞–∫—Ç–∏—á–µ—Å–∫–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ nums1.
    :param n: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ nums2.
    :return: None - –º–∞—Å—Å–∏–≤ nums1 –∏–∑–º–µ–Ω—è–µ—Ç—Å—è –Ω–∞ –º–µ—Å—Ç–µ.
    """

    i: int = m - 1  # –ò–Ω–¥–µ–∫—Å –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –≤ nums1 (–∏—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ)
    j: int = n - 1  # –ò–Ω–¥–µ–∫—Å –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ –≤ nums2
    k: int = m + n - 1  # –ò–Ω–¥–µ–∫—Å –ø–æ—Å–ª–µ–¥–Ω–µ–π –ø–æ–∑–∏—Ü–∏–∏ –≤ nums1 (–∫—É–¥–∞ –≤—Å—Ç–∞–≤–ª—è—Ç—å —ç–ª–µ–º–µ–Ω—Ç—ã)

    # –î–≤–∏–≥–∞–µ–º—Å—è —Å –∫–æ–Ω—Ü–∞, —á—Ç–æ–±—ã –Ω–µ –∑–∞—Ç–∏—Ä–∞—Ç—å —ç–ª–µ–º–µ–Ω—Ç—ã –≤ nums1
    while j >= 0:
        if i >= 0 and nums1[i] > nums2[j]:
            nums1[k] = nums1[i]  # –ï—Å–ª–∏ —ç–ª–µ–º–µ–Ω—Ç –≤ nums1 –±–æ–ª—å—à–µ, –ø–µ—Ä–µ–º–µ—â–∞–µ–º –µ–≥–æ
            i -= 1
        else:
            nums1[k] = nums2[j]  # –ò–Ω–∞—á–µ –±–µ—Ä–µ–º —ç–ª–µ–º–µ–Ω—Ç –∏–∑ nums2
            j -= 1
        k -= 1  # –ü–µ—Ä–µ—Ö–æ–¥ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É –º–µ—Å—Ç—É

# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
nums1 = [1, 3, 5, 0, 0, 0]
nums2 = [2, 4, 6]
m, n = 3, 3

merge_sorted_arrays(nums1, nums2, m, n)
print(nums1)  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: [1, 2, 3, 4, 5, 6]

