# Задача: Конвертация целого числа в римскую запись.
# Дано целое число num (1 <= num <= 3999).
# Нужно вернуть строку с римским представлением числа.

def int_to_roman(num: int) -> str:
    """
    Функция конвертирует целое число в римскую запись.

    :param num: int - число от 1 до 3999.
    :return: str - римское представление числа.
    """

    # Список римских цифр, упорядоченный от больших к меньшим
    roman_map: list[tuple[int, str]] = [
        (1000, "M"), (900, "CM"), (500, "D"), (400, "CD"),
        (100, "C"), (90, "XC"), (50, "L"), (40, "XL"),
        (10, "X"), (9, "IX"), (5, "V"), (4, "IV"), (1, "I")
    ]

    result: list[str] = []  # Список для формирования римской записи

    # Проходим по каждой паре (значение, римский символ)
    for value, symbol in roman_map:
        while num >= value:  # Пока можем вычитать значение
            num -= value  # Вычитаем из числа
            result.append(symbol)  # Добавляем соответствующий римский символ

    return "".join(result)  # Собираем строку и возвращаем


# Пример вызова функции
print(int_to_roman(2345))  # Ожидаемый результат: "MMCCCXLV"

