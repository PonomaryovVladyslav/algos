# –ù—É–∂–Ω–æ: –≤–µ—Ä–Ω—É—Ç—å —Å—Ç—Ä–æ–∫—É, –≤ –∫–æ—Ç–æ—Ä–æ–π –ø–æ—Ä—è–¥–æ–∫ —Å–ª–æ–≤ –∏–∑–º–µ–Ω–µ–Ω –Ω–∞ –æ–±—Ä–∞—Ç–Ω—ã–π, –∞ –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã —É–¥–∞–ª–µ–Ω—ã.

def reverse_words(s: str) -> str:
    """
    –§—É–Ω–∫—Ü–∏—è —Ä–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç –ø–æ—Ä—è–¥–æ–∫ —Å–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ.

    :param s: str - –≤—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞.
    :return: str - —Å—Ç—Ä–æ–∫–∞ —Å –ø–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç—ã–º –ø–æ—Ä—è–¥–∫–æ–º —Å–ª–æ–≤.
    """

    return " ".join(s.strip().split()[::-1])  # –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã, —Ä–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ª–æ–≤–∞, –ø–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –∏—Ö –∏ –æ–±—ä–µ–¥–∏–Ω—è–µ–º –æ–±—Ä–∞—Ç–Ω–æ


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
s1 = "the sky is blue"
s2 = "  hello world  "
s3 = "a good   example"

print(reverse_words(s1))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "blue is sky the"
print(reverse_words(s2))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "world hello"
print(reverse_words(s3))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "example good a"

