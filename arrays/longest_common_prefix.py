# –ù—É–∂–Ω–æ –≤–µ—Ä–Ω—É—Ç—å —Å–∞–º—É—é –¥–ª–∏–Ω–Ω—É—é –Ω–∞—á–∞–ª—å–Ω—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —Å–∏–º–≤–æ–ª–æ–≤, –æ–±—â—É—é –¥–ª—è –≤—Å–µ—Ö —Å—Ç—Ä–æ–∫.

def longest_common_prefix(strs: list[str]) -> str:
    """
    –§—É–Ω–∫—Ü–∏—è –Ω–∞—Ö–æ–¥–∏—Ç —Å–∞–º—ã–π –¥–ª–∏–Ω–Ω—ã–π –æ–±—â–∏–π –ø—Ä–µ—Ñ–∏–∫—Å —Å—Ä–µ–¥–∏ —Å–ø–∏—Å–∫–∞ —Å—Ç—Ä–æ–∫.

    :param strs: list[str] - —Å–ø–∏—Å–æ–∫ —Å—Ç—Ä–æ–∫.
    :return: str - –æ–±—â–∏–π –ø—Ä–µ—Ñ–∏–∫—Å, –ª–∏–±–æ –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞, –µ—Å–ª–∏ –ø—Ä–µ—Ñ–∏–∫—Å–∞ –Ω–µ—Ç.
    """

    if not strs:
        return ""  # –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É

    prefix: str = strs[0]  # –ë–µ—Ä–µ–º –ø–µ—Ä–≤—É—é —Å—Ç—Ä–æ–∫—É –∫–∞–∫ –Ω–∞—á–∞–ª—å–Ω—ã–π –ø—Ä–µ—Ñ–∏–∫—Å

    # –°—Ä–∞–≤–Ω–∏–≤–∞–µ–º –ø—Ä–µ—Ñ–∏–∫—Å —Å –∫–∞–∂–¥–æ–π —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–æ–π
    for s in strs[1:]:
        while prefix and prefix != s[:len(prefix)]:  # –ü–æ–∫–∞ –ø—Ä–µ—Ñ–∏–∫—Å –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç
            prefix = prefix[:-1]  # –û–±—Ä–µ–∑–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π —Å–∏–º–≤–æ–ª

    return prefix  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–∞–π–¥–µ–Ω–Ω—ã–π –æ–±—â–∏–π –ø—Ä–µ—Ñ–∏–∫—Å


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
strs = ["flower", "flow", "flight"]
print(longest_common_prefix(strs))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "fl"

strs2 = ["dog", "racecar", "car"]
print(longest_common_prefix(strs2))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "" (–Ω–µ—Ç –æ–±—â–µ–≥–æ –ø—Ä–µ—Ñ–∏–∫—Å–∞)

strs3 = ["interview", "internet", "internal"]
print(longest_common_prefix(strs3))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: "inte"

