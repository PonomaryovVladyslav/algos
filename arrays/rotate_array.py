# –ó–∞–¥–∞—á–∞: –ü–æ–≤–µ—Ä–Ω—É—Ç—å –º–∞—Å—Å–∏–≤ –≤–ø—Ä–∞–≤–æ –Ω–∞ `k` —à–∞–≥–æ–≤.
# –î–∞–Ω–æ: –º–∞—Å—Å–∏–≤ `nums` –∏ —á–∏—Å–ª–æ `k`.
# –ù—É–∂–Ω–æ: –∏–∑–º–µ–Ω–∏—Ç—å –º–∞—Å—Å–∏–≤ –Ω–∞ –º–µ—Å—Ç–µ —Ç–∞–∫, —á—Ç–æ–±—ã —ç–ª–µ–º–µ–Ω—Ç—ã —Å–¥–≤–∏–Ω—É–ª–∏—Å—å –≤–ø—Ä–∞–≤–æ –Ω–∞ `k` –ø–æ–∑–∏—Ü–∏–π.

def rotate_array(nums: list[int], k: int) -> None:
    """
    –§—É–Ω–∫—Ü–∏—è –≤—ã–ø–æ–ª–Ω—è–µ—Ç —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–π —Å–¥–≤–∏–≥ –º–∞—Å—Å–∏–≤–∞ –≤–ø—Ä–∞–≤–æ –Ω–∞ `k` —à–∞–≥–æ–≤.

    :param nums: list[int] - —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª.
    :param k: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —à–∞–≥–æ–≤ —Å–¥–≤–∏–≥–∞.
    :return: None - –∏–∑–º–µ–Ω–µ–Ω–∏–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –Ω–∞ –º–µ—Å—Ç–µ.
    """

    n: int = len(nums)
    k %= n  # –ï—Å–ª–∏ k > n, —É–º–µ–Ω—å—à–∞–µ–º –µ–≥–æ (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Å–¥–≤–∏–≥ –Ω–∞ 10 –≤ –º–∞—Å—Å–∏–≤–µ –¥–ª–∏–Ω—ã 7 —ç–∫–≤–∏–≤–∞–ª–µ–Ω—Ç–µ–Ω —Å–¥–≤–∏–≥—É –Ω–∞ 3)

    def reverse(start: int, end: int) -> None:
        """–†–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç —á–∞—Å—Ç—å –º–∞—Å—Å–∏–≤–∞ –º–µ–∂–¥—É –∏–Ω–¥–µ–∫—Å–∞–º–∏ start –∏ end."""
        while start < end:
            nums[start], nums[end] = nums[end], nums[start]
            start += 1
            end -= 1

    # 1. –†–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –≤–µ—Å—å –º–∞—Å—Å–∏–≤
    reverse(0, n - 1)
    # 2. –†–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ k —ç–ª–µ–º–µ–Ω—Ç–æ–≤
    reverse(0, k - 1)
    # 3. –†–∞–∑–≤–æ—Ä–∞—á–∏–≤–∞–µ–º –æ—Å—Ç–∞–≤—à—É—é—Å—è —á–∞—Å—Ç—å
    reverse(k, n - 1)


