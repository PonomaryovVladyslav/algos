# –ó–∞–¥–∞—á–∞: –ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è —Ä–∏–º—Å–∫–æ–≥–æ —á–∏—Å–ª–∞ –≤ —Ü–µ–ª–æ–µ.
# –î–∞–Ω–æ: —Å—Ç—Ä–æ–∫–∞ `s`, –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è—é—â–∞—è —á–∏—Å–ª–æ –≤ —Ä–∏–º—Å–∫–æ–π —Å–∏—Å—Ç–µ–º–µ.
# –ù—É–∂–Ω–æ: –≤–µ—Ä–Ω—É—Ç—å —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ —Ä–∏–º—Å–∫–æ–≥–æ —á–∏—Å–ª–∞.

def roman_to_int(s: str) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç —Ä–∏–º—Å–∫–æ–µ —á–∏—Å–ª–æ –≤ —Ü–µ–ª–æ–µ.

    :param s: str - —Å—Ç—Ä–æ–∫–∞ —Å —Ä–∏–º—Å–∫–∏–º —á–∏—Å–ª–æ–º.
    :return: int - —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ —Ä–∏–º—Å–∫–æ–≥–æ —á–∏—Å–ª–∞.
    """

    roman: dict[str, int] = {
        'I': 1, 'V': 5, 'X': 10, 'L': 50, 'C': 100, 'D': 500, 'M': 1000
    }

    total: int = 0

    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å—Ç—Ä–æ–∫–µ, –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—è —Å–∏–º–≤–æ–ª—ã —Ä–∏–º—Å–∫–æ–≥–æ —á–∏—Å–ª–∞
    for i in range(len(s)):
        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —Å–∏–º–≤–æ–ª –º–µ–Ω—å—à–µ —Å–ª–µ–¥—É—é—â–µ–≥–æ, –∑–Ω–∞—á–∏—Ç, —ç—Ç–æ –≤—ã—á–∏—Ç–∞—Ç–µ–ª—å–Ω–∞—è –ø–∞—Ä–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, IV = 4)
        if i < len(s) - 1 and roman[s[i]] < roman[s[i + 1]]:
            total -= roman[s[i]]  # –í—ã—á–∏—Ç–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ
        else:
            total += roman[s[i]]  # –í –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —Å–ª—É—á–∞—è—Ö –ø—Ä–∏–±–∞–≤–ª—è–µ–º

    return total


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(roman_to_int("III"))    # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 3
print(roman_to_int("IV"))     # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 4
print(roman_to_int("IX"))     # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 9
print(roman_to_int("LVIII"))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 58
print(roman_to_int("MCMXCIV"))# –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 1994

