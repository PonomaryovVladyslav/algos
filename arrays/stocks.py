# –ó–∞–¥–∞—á–∞: –ù–∞–π—Ç–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –ø—Ä–∏–±—ã–ª—å –æ—Ç –ø–æ–∫—É–ø–∫–∏ –∏ –ø—Ä–æ–¥–∞–∂–∏ –∞–∫—Ü–∏–∏.
# –î–∞–Ω–æ: —Å–ø–∏—Å–æ–∫ —Ü–µ–Ω `prices`, –≥–¥–µ `prices[i]` ‚Äî —Ü–µ–Ω–∞ –∞–∫—Ü–∏–∏ –≤ –¥–µ–Ω—å `i`.
# –ù—É–∂–Ω–æ: –Ω–∞–π—Ç–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —Ä–∞–∑–Ω–∏—Ü—É –º–µ–∂–¥—É —Ü–µ–Ω–æ–π –ø—Ä–æ–¥–∞–∂–∏ –∏ –ø–æ–∫—É–ø–∫–∏ (–ø–æ–∫—É–ø–∫–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —Ä–∞–Ω—å—à–µ –ø—Ä–æ–¥–∞–∂–∏).

def max_profit(prices: list[int]) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –≤—ã—á–∏—Å–ª—è–µ—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é –ø—Ä–∏–±—ã–ª—å –æ—Ç –æ–¥–Ω–æ–π –ø–æ–∫—É–ø–∫–∏ –∏ –æ–¥–Ω–æ–π –ø—Ä–æ–¥–∞–∂–∏ –∞–∫—Ü–∏–∏.

    :param prices: list[int] - —Å–ø–∏—Å–æ–∫ —Ü–µ–Ω –∞–∫—Ü–∏–∏ –ø–æ –¥–Ω—è–º.
    :return: int - –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –≤–æ–∑–º–æ–∂–Ω–∞—è –ø—Ä–∏–±—ã–ª—å.
    """

    if not prices:
        return 0  # –ï—Å–ª–∏ —Å–ø–∏—Å–æ–∫ –ø—É—Å—Ç, –ø—Ä–∏–±—ã–ª–∏ –Ω–µ—Ç

    min_price: int = float('inf')  # –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —Ü–µ–Ω–∞ –¥–ª—è –ø–æ–∫—É–ø–∫–∏
    max_profit: int = 0  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –≤–æ–∑–º–æ–∂–Ω–∞—è –ø—Ä–∏–±—ã–ª—å

    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å–ø–∏—Å–∫—É –∏ –æ–±–Ω–æ–≤–ª—è–µ–º min_price –∏ max_profit
    for price in prices:
        min_price = min(min_price, price)  # –û–±–Ω–æ–≤–ª—è–µ–º –º–∏–Ω–∏–º–∞–ª—å–Ω—É—é —Ü–µ–Ω—É –ø–æ–∫—É–ø–∫–∏
        max_profit = max(max_profit, price - min_price)  # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–µ–∫—É—â—É—é –ø—Ä–∏–±—ã–ª—å

    return max_profit


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(max_profit([1, 100, 0, 50]))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 50 (–ø–æ–∫—É–ø–∫–∞ –ø–æ 0, –ø—Ä–æ–¥–∞–∂–∞ –ø–æ 50)
print(max_profit([7, 1, 5, 3, 6, 4]))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 5 (–ø–æ–∫—É–ø–∫–∞ –ø–æ 1, –ø—Ä–æ–¥–∞–∂–∞ –ø–æ 6)
print(max_profit([7, 6, 4, 3, 1]))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 0 (–Ω–µ—Ç –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–∏ –ø–æ–ª—É—á–∏—Ç—å –ø—Ä–∏–±—ã–ª—å)

"""
–ê–Ω–∞–ª–∏–∑ —Å–ª–æ–∂–Ω–æ—Å—Ç–∏:
- –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(n), —Ç–∞–∫ –∫–∞–∫ –º—ã –ø—Ä–æ—Ö–æ–¥–∏–º –º–∞—Å—Å–∏–≤ –æ–¥–∏–Ω —Ä–∞–∑.
- –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: O(1), —Ç–∞–∫ –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–æ–ª—å–∫–æ –¥–≤–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ.
"""
