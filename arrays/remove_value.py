# –ù—É–∂–Ω–æ: —É–¥–∞–ª–∏—Ç—å –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã `val` –Ω–∞ –º–µ—Å—Ç–µ –∏ –≤–µ—Ä–Ω—É—Ç—å –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤.

def remove_element(nums: list[int], val: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è —É–¥–∞–ª—è–µ—Ç –≤—Å–µ –≤—Ö–æ–∂–¥–µ–Ω–∏—è —á–∏—Å–ª–∞ `val` –∏–∑ —Å–ø–∏—Å–∫–∞ `nums` –Ω–∞ –º–µ—Å—Ç–µ.

    :param nums: list[int] - —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª.
    :param val: int - —É–¥–∞–ª—è–µ–º—ã–π —ç–ª–µ–º–µ–Ω—Ç.
    :return: int - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è –≤ –º–∞—Å—Å–∏–≤–µ –ø–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è `val`.
    """

    k: int = 0  # –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –º–µ—Å—Ç–æ, –∫—É–¥–∞ –≤—Å—Ç–∞–≤–ª—è—Ç—å –æ—Å—Ç–∞–≤—à–∏–µ—Å—è —ç–ª–µ–º–µ–Ω—Ç—ã

    for i in range(len(nums)):
        if nums[i] != val:  # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —ç–ª–µ–º–µ–Ω—Ç –Ω–µ —Ä–∞–≤–µ–Ω `val`
            nums[k] = nums[i]  # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –µ–≥–æ –Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ –º–µ—Å—Ç–æ
            k += 1  # –î–≤–∏–≥–∞–µ–º —É–∫–∞–∑–∞—Ç–µ–ª—å –¥–∞–ª—å—à–µ

    return k  # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è –≤ –º–∞—Å—Å–∏–≤–µ


