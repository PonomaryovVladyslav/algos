# Задача: Найти длину последнего слова в строке.
# Дано: строка s, содержащая слова, разделенные пробелами.
# Нужно вернуть длину последнего слова (слова разделены пробелами, могут быть лишние пробелы в начале и конце).

def last_word_len(s: str) -> int:
    """
    Функция находит длину последнего слова в строке.

    :param s: str - входная строка.
    :return: int - длина последнего слова.
    """

    return len(
        s.strip().split()[-1])  # Убираем пробелы, делим строку на слова, берем последнее слово и измеряем его длину.


# Примеры вызова функции
s1 = "Hello World"
s2 = "   fly me   to   the moon  "
s3 = "luffy is still joyboy"

print(last_word_len(s1))  # Ожидаемый результат: 5
print(last_word_len(s2))  # Ожидаемый результат: 4
print(last_word_len(s3))  # Ожидаемый результат: 6

