# –ó–∞–¥–∞—á–∞: –ù–∞–π—Ç–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —Å—É–º–º—É –ø–æ–¥–º–∞—Å—Å–∏–≤–∞ —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ–π –¥–ª–∏–Ω—ã `k`.
# –î–∞–Ω–æ: —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª `nums` –∏ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ `k`.
# –ù—É–∂–Ω–æ –Ω–∞–π—Ç–∏ –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —Å—É–º–º—É `k` –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ `nums`.

def max_subarray_sum(nums: list[int], k: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –Ω–∞—Ö–æ–¥–∏—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—É—é —Å—É–º–º—É –ø–æ–¥–º–∞—Å—Å–∏–≤–∞ –¥–ª–∏–Ω–æ–π `k`.

    :param nums: list[int] - —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª.
    :param k: int - –¥–ª–∏–Ω–∞ –ø–æ–¥–º–∞—Å—Å–∏–≤–∞.
    :return: int - –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –ø–æ–¥–º–∞—Å—Å–∏–≤–∞ –¥–ª–∏–Ω–æ–π `k`.
    """

    if len(nums) < k:
        return 0  # –ï—Å–ª–∏ –º–∞—Å—Å–∏–≤ –∫–æ—Ä–æ—á–µ `k`, –≤–µ—Ä–Ω—É—Ç—å 0

    s: int = sum(nums[:k])  # –ù–∞—á–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –ø–µ—Ä–≤—ã—Ö `k` —ç–ª–µ–º–µ–Ω—Ç–æ–≤
    max_sum: int = s  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞, –Ω–∞—á–∏–Ω–∞–µ–º —Å –ø–µ—Ä–≤–æ–π `k`-–≥—Ä—É–ø–ø—ã

    # –ò—Å–ø–æ–ª—å–∑—É–µ–º —Å–∫–æ–ª—å–∑—è—â–µ–µ –æ–∫–Ω–æ: –¥–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç, —É–±–∏—Ä–∞–µ–º —Å—Ç–∞—Ä—ã–π
    for i in range(k, len(nums)):
        s += nums[i] - nums[i - k]  # –û–±–Ω–æ–≤–ª—è–µ–º —Å—É–º–º—É –æ–∫–Ω–∞
        max_sum = max(max_sum, s)  # –û–±–Ω–æ–≤–ª—è–µ–º –º–∞–∫—Å–∏–º—É–º, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ

    return max_sum


