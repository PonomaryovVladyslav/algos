# –ó–∞–¥–∞—á–∞: –ù–∞–π—Ç–∏ –∏–Ω–¥–µ–∫—Å `target` –≤ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –º–∞—Å—Å–∏–≤–µ `nums` –∏–ª–∏ –ø–æ–∑–∏—Ü–∏—é, –∫—É–¥–∞ –µ–≥–æ –º–æ–∂–Ω–æ –≤—Å—Ç–∞–≤–∏—Ç—å.
# –î–∞–Ω–æ: –º–∞—Å—Å–∏–≤ `nums`, –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é, –∏ —á–∏—Å–ª–æ `target`.
# –ù—É–∂–Ω–æ: –≤–µ—Ä–Ω—É—Ç—å –∏–Ω–¥–µ–∫—Å `target`, –µ—Å–ª–∏ –æ–Ω –Ω–∞–π–¥–µ–Ω, –∏–Ω–∞—á–µ –≤–µ—Ä–Ω—É—Ç—å –∏–Ω–¥–µ–∫—Å, –∫—É–¥–∞ –µ–≥–æ –º–æ–∂–Ω–æ –≤—Å—Ç–∞–≤–∏—Ç—å.

def search_insert(nums: list[int], target: int) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –≤—ã–ø–æ–ª–Ω—è–µ—Ç –±–∏–Ω–∞—Ä–Ω—ã–π –ø–æ–∏—Å–∫, —á—Ç–æ–±—ã –Ω–∞–π—Ç–∏ `target` –≤ `nums` –∏–ª–∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å –µ–≥–æ –ø–æ–∑–∏—Ü–∏—é –≤—Å—Ç–∞–≤–∫–∏.

    :param nums: list[int] - –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª.
    :param target: int - –∏—Å–∫–æ–º–æ–µ —á–∏—Å–ª–æ.
    :return: int - –∏–Ω–¥–µ–∫—Å `target` –∏–ª–∏ –ø–æ–∑–∏—Ü–∏—è –µ–≥–æ –≤—Å—Ç–∞–≤–∫–∏.
    """

    left, right = 0, len(nums) - 1  # –ì—Ä–∞–Ω–∏—Ü—ã –±–∏–Ω–∞—Ä–Ω–æ–≥–æ –ø–æ–∏—Å–∫–∞

    while left <= right:
        mid = (left + right) // 2  # –°—Ä–µ–¥–Ω–∏–π –∏–Ω–¥–µ–∫—Å

        if nums[mid] == target:
            return mid  # –ù–∞–π–¥–µ–Ω–æ —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
        elif nums[mid] < target:
            left = mid + 1  # –ò—â–µ–º —Å–ø—Ä–∞–≤–∞
        else:
            right = mid - 1  # –ò—â–µ–º —Å–ª–µ–≤–∞

    return left  # `left` —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ –º–µ—Å—Ç–æ –≤—Å—Ç–∞–≤–∫–∏


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(search_insert([1, 3, 5, 6], 5))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 2
print(search_insert([1, 3, 5, 6], 2))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 1
print(search_insert([1, 3, 5, 6], 7))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 4
print(search_insert([1, 3, 5, 6], 0))  # –û–∂–∏–¥–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç: 0
