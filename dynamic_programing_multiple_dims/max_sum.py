def min_path_sum(grid: list[list[int]]) -> int:
    """
    –§—É–Ω–∫—Ü–∏—è –≤—ã—á–∏—Å–ª—è–µ—Ç **–º–∏–Ω–∏–º–∞–ª—å–Ω—É—é —Å—É–º–º—É –ø—É—Ç–∏** –æ—Ç `grid[0][0]` –¥–æ `grid[m-1][n-1]`,
    –¥–≤–∏–≥–∞—è—Å—å —Ç–æ–ª—å–∫–æ **–≤–Ω–∏–∑** –∏–ª–∏ **–≤–ø—Ä–∞–≤–æ**.

    üîπ –ê–ª–≥–æ—Ä–∏—Ç–º (–î–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–µ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ O(m * n)):
    1Ô∏è‚É£ –ò—Å–ø–æ–ª—å–∑—É–µ–º `grid[i][j]`, –≥–¥–µ `grid[i][j]` ‚Äî –º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –ø—É—Ç–∏ –¥–æ `grid[i][j]`.
    2Ô∏è‚É£ –ó–∞–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–≤—É—é —Å—Ç—Ä–æ–∫—É: `grid[0][j] += grid[0][j-1]` (—Ç–æ–ª—å–∫–æ —Å–ø—Ä–∞–≤–∞).
    3Ô∏è‚É£ –ó–∞–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–≤—ã–π —Å—Ç–æ–ª–±–µ—Ü: `grid[i][0] += grid[i-1][0]` (—Ç–æ–ª—å–∫–æ –≤–Ω–∏–∑).
    4Ô∏è‚É£ –î–ª—è –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è —è—á–µ–µ–∫:
       `grid[i][j] += min(grid[i-1][j], grid[i][j-1])`
    5Ô∏è‚É£ –ò—Ç–æ–≥–æ–≤—ã–π –æ—Ç–≤–µ—Ç: `grid[m-1][n-1]`.

    üîπ –í—Ä–µ–º–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(m * n)** ‚Äî –¥–≤–æ–π–Ω–æ–π —Ü–∏–∫–ª.
    üîπ –ü—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–µ–Ω–Ω–∞—è —Å–ª–æ–∂–Ω–æ—Å—Ç—å: **O(1)** ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ–º `grid` –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è `dp`.

    :param grid: list[list[int]] - `m x n` –º–∞—Ç—Ä–∏—Ü–∞ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª.
    :return: int - –º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –ø—É—Ç–∏.
    """

    m, n = len(grid), len(grid[0])

    # –ó–∞–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–≤—É—é —Å—Ç—Ä–æ–∫—É (–∏–¥—ë–º —Ç–æ–ª—å–∫–æ –≤–ø—Ä–∞–≤–æ)
    for j in range(1, n):
        grid[0][j] += grid[0][j - 1]

    # –ó–∞–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–≤—ã–π —Å—Ç–æ–ª–±–µ—Ü (–∏–¥—ë–º —Ç–æ–ª—å–∫–æ –≤–Ω–∏–∑)
    for i in range(1, m):
        grid[i][0] += grid[i - 1][0]

    # –ó–∞–ø–æ–ª–Ω—è–µ–º –æ—Å—Ç–∞–≤—à–∏–µ—Å—è —è—á–µ–π–∫–∏ (–≤—ã–±–∏—Ä–∞–µ–º –º–∏–Ω–∏–º—É–º)
    for i in range(1, m):
        for j in range(1, n):
            grid[i][j] += min(grid[i - 1][j], grid[i][j - 1])

    return grid[-1][-1]  # –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –≤ –ø—Ä–∞–≤–æ–º –Ω–∏–∂–Ω–µ–º —É–≥–ª—É


# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(min_path_sum([
  [1, 3, 1],
  [1, 5, 1],
  [4, 2, 1]
]))  # ‚úÖ 7

print(min_path_sum([
  [1, 2, 3],
  [4, 5, 6]
]))  # ‚úÖ 12

print(min_path_sum([
  [1, 2],
  [1, 1]
]))  # ‚úÖ 3
