def calculate(s: str) -> int:
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–µ—Å–∫–æ–≥–æ –≤—ã—Ä–∞–∂–µ–Ω–∏—è, —Å–æ–¥–µ—Ä–∂–∞—â–µ–≥–æ +, -, ().

    üîπ **–ú–µ—Ç–æ–¥**: –ò—Å–ø–æ–ª—å–∑—É–µ–º —Å—Ç–µ–∫ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–∫–æ–±–æ–∫
    üîπ **–°–ª–æ–∂–Ω–æ—Å—Ç—å**: O(n) ‚Äî –æ–¥–∏–Ω –ø—Ä–æ—Ö–æ–¥ –ø–æ —Å—Ç—Ä–æ–∫–µ
    üîπ **–ü–∞–º—è—Ç—å**: O(n) ‚Äî —Å—Ç–µ–∫ –º–æ–∂–µ—Ç —Ö—Ä–∞–Ω–∏—Ç—å –≤—Å–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–µ —Å–∫–æ–±–∫–∏

    ‚úÖ **–ü—Ä–∏–º–µ—Ä —Ä–∞–±–æ—Ç—ã**:
       ```
       –í—Ö–æ–¥: "1 + 1" ‚Üí –í—ã—Ö–æ–¥: 2
       –í—Ö–æ–¥: " 2-1 + 2 " ‚Üí –í—ã—Ö–æ–¥: 3
       –í—Ö–æ–¥: "(1+(4+5+2)-3)+(6+8)" ‚Üí –í—ã—Ö–æ–¥: 23
       –í—Ö–æ–¥: "10 - (2 + 3)" ‚Üí –í—ã—Ö–æ–¥: 5
       –í—Ö–æ–¥: "((1+2)+3)" ‚Üí –í—ã—Ö–æ–¥: 6
       ```
    """
    stack = []
    num = 0
    result = 0  # –¢–µ–∫—É—â–∞—è —Å—É–º–º–∞
    sign = 1  # 1 = "+", -1 = "-"

    for char in s:
        if char.isdigit():  # –°–æ–±–∏—Ä–∞–µ–º —á–∏—Å–ª–æ
            num = num * 10 + int(char)
        elif char in "+-":  # –û–ø–µ—Ä–∞—Ç–æ—Ä
            result += sign * num  # –î–æ–±–∞–≤–ª—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–µ–µ —á–∏—Å–ª–æ
            num = 0
            sign = 1 if char == "+" else -1  # –û–±–Ω–æ–≤–ª—è–µ–º –∑–Ω–∞–∫
        elif char == "(":  # –û—Ç–∫—Ä—ã–≤–∞—é—â–∞—è —Å–∫–æ–±–∫–∞
            stack.append(result)  # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ç–µ–∫—É—â—É—é —Å—É–º–º—É
            stack.append(sign)  # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ç–µ–∫—É—â–∏–π –∑–Ω–∞–∫
            result = 0
            sign = 1
        elif char == ")":  # –ó–∞–∫—Ä—ã–≤–∞—é—â–∞—è —Å–∫–æ–±–∫–∞
            result += sign * num  # –û–±–Ω–æ–≤–ª—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Å–∫–æ–±–∫–∏
            result *= stack.pop()  # –ü—Ä–∏–º–µ–Ω—è–µ–º –∑–Ω–∞–∫ –ø–µ—Ä–µ–¥ —Å–∫–æ–±–∫–∞–º–∏
            result += stack.pop()  # –î–æ–±–∞–≤–ª—è–µ–º —Å—É–º–º—É –¥–æ —Å–∫–æ–±–æ–∫
            num = 0

    return result + sign * num  # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ —á–∏—Å–ª–æ

# üîπ –¢–µ—Å—Ç–∏—Ä—É–µ–º
print(calculate("1 + 1"))  # ‚úÖ 2
print(calculate(" 2-1 + 2 "))  # ‚úÖ 3
print(calculate("(1+(4+5+2)-3)+(6+8)"))  # ‚úÖ 23
print(calculate("10 - (2 + 3)"))  # ‚úÖ 5
print(calculate("100"))  # ‚úÖ 100
print(calculate("(10)"))  # ‚úÖ 10
print(calculate("((1+2)+3)"))  # ‚úÖ 6
